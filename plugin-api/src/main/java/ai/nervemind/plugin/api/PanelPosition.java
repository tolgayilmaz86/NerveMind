package ai.nervemind.plugin.api;

/**
 * Specifies the position where a side panel should appear.
 * 
 * @author NerveMind Team
 * @since 1.0.0
 * @see SidePanelContribution
 */
public enum PanelPosition {

    /**
     * Panel appears on the left side of the main canvas.
     */
    LEFT,

    /**
     * Panel appears on the right side of the main canvas.
     */
    RIGHT,

    /**
     * Panel appears at the bottom below the main canvas.
     */
    BOTTOM
}
